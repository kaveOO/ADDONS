if CLIENT then

    surface.CreateFont( "Dangerosite:Responsive" , {

        font = "Japan_wave",
        size = 30 * ( ScrH() / 1080 ),
        weight = 1000,
        shadow = true,
        antialias = true,
        italic = true,
    
    } )
    
    hook.Add("PostPlayerDraw", "Pymousss:HUD3D", function(ply)
            
        if not IsValid(ply) or not ply:Alive() then return end

        local activeWeapon = ply:GetActiveWeapon()
        if not IsValid(activeWeapon) then return end

        if not PymousssHUD3DConfig["Weapons - Config"][activeWeapon:GetClass()] then return end

        if not PymousssHUD3DConfig["Job - Config"][team.GetName(ply:Team())] then return end

        local dist = LocalPlayer():GetPos():Distance(ply:GetPos())

        if dist < PymousssHUD3DConfig["View - Config"].config then

            local offset = Vector(0, 0, 85)
            local ang = LocalPlayer():EyeAngles()
            local pos = ply:GetPos() + offset + ang:Up()
            local info = PymousssHUD3DConfig["Job - Config"][team.GetName(ply:Team())]

            ang:RotateAroundAxis(ang:Forward(), 90)
            ang:RotateAroundAxis(ang:Right(), 90)

            cam.Start3D2D(pos, Angle(0, ang.y, 90), 0.25)
                draw.SimpleTextOutlined(info, "Dangerosite:Responsive", 0, 0, PymousssHUD3DConfig["Color - Config"][info], TEXT_ALIGN_CENTER, TEXT_ALIGN_CENTER, 1, Color(0, 0, 0, 255))
            cam.End3D2D()

        end
            
   	end )

